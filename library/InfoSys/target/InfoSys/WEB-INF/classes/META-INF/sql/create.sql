SET
  UNIQUE_CHECKS = 0;
SET
  FOREIGN_KEY_CHECKS = 0;
 
CREATE SCHEMA IF NOT EXISTS `library` DEFAULT CHARACTER SET utf8;
USE `library`;

CREATE TABLE IF NOT EXISTS `library`.`member` (
    `ID` INT(11) NOT NULL AUTO_INCREMENT,
    `NAME` VARCHAR(255) NOT NULL,
    `PHONE_NUMBER` VARCHAR(15) NOT NULL,
    `AUTH_CARD_NUMBER` CHAR(8) NOT NULL,
    `ADDRESS` VARCHAR(255) NOT NULL,
    PRIMARY KEY (`ID`),
    UNIQUE (`PHONE_NUMBER`),
    UNIQUE (`AUTH_CARD_NUMBER`)
  ) ENGINE = InnoDB AUTO_INCREMENT = 1 DEFAULT CHARACTER SET = utf8;

CREATE TABLE IF NOT EXISTS `library`.`media` (
    `ID` INT(11) NOT NULL AUTO_INCREMENT,
    `TITLE` VARCHAR(255) NULL,
    `AUTHOR` VARCHAR(255) NULL,
    `TYPE` VARCHAR(255) NULL,
    `SOURCE_DATE` DATE NULL,
    `STATUS` INT NOT NULL DEFAULT 0,
    `MEMBER` INT NULL,
    PRIMARY KEY (`ID`),
    FOREIGN KEY (`MEMBER`) REFERENCES `library`.`member`(`ID`)
  ) ENGINE = InnoDB DEFAULT CHARACTER SET = utf8;

SET
  UNIQUE_CHECKS = 1;
SET
  FOREIGN_KEY_CHECKS = 1;